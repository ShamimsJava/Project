    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if(jComboBox1.getSelectedIndex()>0){
            Connection con = null;
            CallableStatement csmt = null;
            ResultSet rs = null;
            try {
                con = DbConnection.getConnection();
                csmt = con.prepareCall("{? = call getProductQty(?)}");
                csmt.registerOutParameter(1, java.sql.Types.INTEGER);
                csmt.setString(2, jComboBox1.getSelectedItem().toString());
                csmt.execute();
                int output = csmt.getInt(1);
                jLabel7.setText(String.valueOf(output));
                
                /////
                csmt = con.prepareCall("{CALL avg_price(getProductId(?))}");
                csmt.setString(1, jComboBox1.getSelectedItem().toString());
                csmt.execute();
                rs = csmt.getResultSet();
                while(rs.next()){
                    jLabel8.setText(rs.getString(1));
                }
            } catch (Exception e) {
                e.printStackTrace();
            }finally{
                try {
                    rs.close();
                    csmt.close();
                    con.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

